<h3>What is this?</h3>
<p>Competition Squared is a extremely simple, highly customizable, online score management system. It is currently targeted towards <a href="http://www.uiltexas.org/">UIL</a> Meets/Competitions, but we
are definitelly looking into more fields in the near future.</p>
<h3>What can I expect?</h3>
<ul style="padding-left:20px">
	<li>Management of Teams</li>
        <li>Management of Schools</li>
        <li>Management of Events</li>
        <li>Place Calculation</li>
	<li>Custom Scorecards</li>
	<li>Custom Layouts</li>
        <li>Extensive User Management (Administrator, Account Creator, and Parent accounts)</li>
        <li>Live Slideshow of Scores</li>
        <li>Limit Users to Schools</li>
        <li>Mass Parent Account Creation</li>
        <li>Import from <a href="http://joyoftournaments.com/">Joy Of Tournaments</a></li>
	<li>Data Backup/Retension</li>
	<li>...and more on the way!</li>
</ul>
